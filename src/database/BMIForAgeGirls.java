	public String getBMIForGirlsStatus(int year, int month, float bmi) {
		String result = "Out of Range";
		try {
			conn = connect();
			String query = "select status from BMIForAgeGirls "
					+ "	where "
					+ "		year = ? "
					+ "		and month = ?"
					+ "		and ? > min"
					+ "		and ? < max";
			
			PreparedStatement stmt = conn.prepareStatement(query);
			stmt.setInt(1, year);
			stmt.setInt(2, month);
			stmt.setFloat(3, bmi);
			stmt.setFloat(4, bmi);
			

			ResultSet res = stmt.executeQuery();
			while(res.next()) {
				result = res.getString("status");
			}

		} catch (Exception e) {
			// TODO: handle exception
		}
		closeConnection(conn);
		if(result == "Out of Range") {
			if(bmi < 18.5 && bmi > 0) {
				return "UNDERWEIGHT";
			}
			else if(bmi >= 18.5 && bmi < 25.0) {
				return "NORMAL";
			}
			else if(bmi >= 25.0 && bmi < 30.0) {
				return "OVERWEIGHT";
			}
			else if(bmi > 30.0) {
				return "OBESE";
			}
		}
		return result;
	}
